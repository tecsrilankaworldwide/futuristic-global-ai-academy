// Bilingual postProcessor - shows translated text + English when language is not English
const bilingualPostProcessor = {
  type: 'postProcessor',
  name: 'bilingual',
  process: function(value, key, options, translator) {
    const currentLang = translator.language || i18n.language;
    if (currentLang === 'en') return value;
    
    // Get the English version without triggering postProcessor again
    const englishValue = i18n.t(key, { ...options, lng: 'en', postProcess: [] });
    
    // If same text (no translation), just return it
    if (value === englishValue) return value;
    
    // Return bilingual: Translated / English
    return `${value} / ${englishValue}`;
  }
};

